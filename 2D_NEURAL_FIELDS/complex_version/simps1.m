function S=simps1(f,N,m,n,ll,h)


ex = @(x,y)(exp((-1j*pi)*((m*x)./ll+(n*y)./h)));

Smn=[1,4,2,4,2,4,2,4,1;

     4,16,8,16,8,16,8,16,4;

     2,8,4,8,4,8,4,8,2;
     
     4,16,8,16,8,16,8,16,4;

     2,8,4,8,4,8,4,8,2;

     4,16,8,16,8,16,8,16,4;

     2,8,4,8,4,8,4,8,2;

     4,16,8,16,8,16,8,16,4;
    
     1,4,2,4,2,4,2,4,1; 
     ];

S=0;
dx=(pi+pi)./(9-1);
dy=(pi+pi)./(9-1);

for m=1:9
    for n=1:9
    S=S+f(m*50,n*50)*ex(m*50,n*50)*Smn(m,n);
    end
end
S=dx.*dy./9*S;
end
